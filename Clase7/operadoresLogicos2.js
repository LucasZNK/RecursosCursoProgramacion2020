

//* AND(&&) logico
// Devuelve verdadero solo si TODAS las condiciones son verdaderas

let a1 = true && true   //* Devuelve TRUE
let a2 = false && true  //! Devuelve FALSE
let a3 = true && false  //! Devuelve FALSE
let a4 = false & false  //! Devuelve FALSE

//* OR(||) logico
// Devuelve verdadero si almenos UNA de las condiciones es verdadera.

let b1 = true || true   //* Devuelve TRUE
let b2 = false || true  //* Devuelve TRUE
let b3 = true || false  //* Devuelve TRUE
let b4 = false || false  //! Devuelve FALSE

//* NOT(!) logico
// niega un valor

let c1 = !false //* Devuelve TRUE
let c2 = !true //! FALSE


